void delete_element_int(int arr[], int n, int x)
{
    int i;
    for (i = 0; i < n; i++) //n = size of array + 1
        if (arr[i] == x)    //search x in array
            break;
    if (i < n) //if found in array
    {
        n = n - 1;
        for (int j = i; j < n; j++)
            arr[j] = arr[j + 1]; //move element to front
    }
};
void delete_element_string(string arr[], int n, string x)
{
    int i;
    for (i = 0; i < n; i++)
        if (arr[i] == x)
            break;
    if (i < n)
    {
        n = n - 1;
        for (int j = i; j < n; j++)
            arr[j] = arr[j + 1];
    }
};
